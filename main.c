void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM3 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2)
    {
        uint32_t current_time = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

        switch(capture_state)
        {
            case 0: // First rising edge
                rising_edge_time = current_time;
                __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_FALLING);
                capture_state = 1;
                break;

            case 1: // Falling edge
                falling_edge_time = current_time;
                __HAL_TIM_SET_CAPTUREPOLARITY(htim, TIM_CHANNEL_2, TIM_INPUTCHANNELPOLARITY_RISING);
                capture_state = 2;
                break;

            case 2: // Next rising edge
                next_rising_edge_time = current_time;

                // Calculate pulse width with overflow handling
                if (falling_edge_time >= rising_edge_time) {
                    pulse_width = falling_edge_time - rising_edge_time;
                } else {
                    pulse_width = ((htim->Init.Period + 1) - rising_edge_time) + falling_edge_time;
                }

                // Calculate period with overflow handling
                if (next_rising_edge_time >= rising_edge_time) {
                    period = next_rising_edge_time - rising_edge_time;
                } else {
                    period = ((htim->Init.Period + 1) - rising_edge_time) + next_rising_edge_time;
                }

                // Calculate frequency and duty cycle
                float timer_clock = SystemCoreClock / (htim->Init.Prescaler + 1);
                frequency = timer_clock / period;
                duty_cycle = (float)(pulse_width * 100) / period;

                // Dynamically adjust timer period
                htim->Init.Period = period;
                HAL_TIM_Base_Init(htim);

                // Reset for next cycle
                capture_state = 0;
                break;
        }
    }
}

